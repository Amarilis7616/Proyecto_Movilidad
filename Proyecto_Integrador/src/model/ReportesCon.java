package model;

public class ReportesCon {
	private int contador;
	private int anio;
	
	
	public ReportesCon() {
		
	}

	public ReportesCon(int contador, int anio){
		this.contador = contador;
		this.anio = anio;
	}

	public int getContador() {
		return contador;
	}

	public void setContador(int contador) {
		this.contador = contador;
	}

	public int getAnio() {
		return anio;
	}

	public void setAnio(int anio) {
		this.anio = anio;
	}

	

}
